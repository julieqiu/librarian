func TestYAMLCommandSchema(t *testing.T) {
	const root = "testdata/parallelstore/surface"
	var files []string
	err := filepath.WalkDir(root, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			return nil
		}
		if filepath.Ext(path) != ".yaml" {
			return nil
		}
		files = append(files, path)
		return nil
	})
	if err != nil {
		t.Fatal(err)
	}

	for _, filename := range files {
		t.Run(filename, func(t *testing.T) {
			data, err := os.ReadFile(filename)
			if err != nil {
				t.Fatal(err)
			}
			data = bytes.ReplaceAll(data, []byte("\r\n"), []byte("\n"))

			if strings.Contains(string(data), "_PARTIALS_") {
				return
			}

			commands, err := yaml.Unmarshal[[]*yamlCommand](data)
			if err != nil {
				t.Fatal(err)
			}

			marshaled, err := yaml.Marshal(commands)
			if err != nil {
				t.Fatal(err)
			}

			roundTripped, err := yaml.Unmarshal[[]*yamlCommand](marshaled)
			if err != nil {
				t.Fatal(err)
			}

			if diff := cmp.Diff(commands, roundTripped); diff != "" {
				t.Errorf("mismatch (-want +got):\n%s", diff)
			}
		})
	}
}

func TestMarshalCommand(t *testing.T) {
	cmd := &Command{
		Arguments: []Argument{
			{
				ArgName: "test-arg",
				Type:    "string",
			},
		},
	}

	b, err := marshalCommand(cmd, "GA")
	if err != nil {
		t.Fatalf("marshalCommand() unexpected error: %v", err)
	}

	got := string(b)
	want := `- release_tracks:
    - GA
  auto_generated: true
  help_text:
    brief: ""
    description: ""
  arguments:
    params:
      - arg_name: test-arg
        help_text: ""
        is_positional: false
        is_primary_resource: false
        required: false
        repeated: false
        type: string
`
	if diff := cmp.Diff(want, got); diff != "" {
		t.Logf("Got:\n%s", got)
		t.Errorf("marshalCommand() mismatch (-want +got):\n%s", diff)
	}
}

func TestWriteCommandFiles(t *testing.T) {
	tmpDir := t.TempDir()
	verb := "create"

	if err := writeCommandFiles(tmpDir, verb, &Command{}, &Command{}, nil); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	expectedFiles := []string{
		filepath.Join(tmpDir, "create.yaml"),
		filepath.Join(tmpDir, "_partials", "_create_beta.yaml"),
		filepath.Join(tmpDir, "_partials", "_create_ga.yaml"),
	}

	for _, f := range expectedFiles {
		if _, err := os.Stat(f); errors.Is(err, fs.ErrNotExist) {
			t.Errorf("expected file %q to be generated", f)
		}
	}
}

func TestWriteCommandFiles_Error(t *testing.T) {
	err := writeCommandFiles("/dev/null/invalid/path", "create", &Command{}, nil, nil)
	if err == nil {
		t.Error("expected error writing to invalid path, got nil")
	}
}

func TestMapCommandToYAML(t *testing.T) {
	cmd := &Command{
		Hidden: true,
		HelpText: HelpText{
			Brief:       "brief",
			Description: "desc",
			Examples:    "ex",
		},
		Collection: []string{"c"},
		APIVersion: "v1",
		Method:     "m",
		Async: &Async{
			Collection:            []string{"ac"},
			ExtractResourceResult: true,
		},
		ReadModifyUpdate: true,
		ResponseIDField:  "id",
		OutputFormat:     "table",
		Arguments: []Argument{
			{
				ArgName: "arg",
				Spec: []ArgSpec{
					{APIField: "s"},
				},
				Choices: []Choice{
					{ArgValue: "av", EnumValue: "ev", HelpText: "ch"},
				},
				ResourceSpec: &ResourceSpec{
					Name:       "n",
					PluralName: "pn",
					Collection: "c",
					Attributes: []Attribute{
						{AttributeName: "attributeName"},
					},
					DisableAutoCompleters: true,
				},
			},
		},
	}

	yCmd := mapCommandToYAML(cmd, "GA")
	if yCmd.AutoGenerated != true {
		t.Errorf("AutoGenerated = false, want true")
	}

	want := &yamlCommand{
		ReleaseTracks: []string{"GA"},
		AutoGenerated: true,
		Hidden:        true,
		HelpText: yamlHelpText{
			Brief:       "brief",
			Description: "desc",
			Examples:    "ex",
		},
		Request: &yamlRequest{
			Collection: []string{"c"},
			APIVersion: "v1",
			Method:     "m",
		},
		Async: &yamlAsync{
			Collection:            []string{"ac"},
			ExtractResourceResult: true,
		},
		Update: &yamlUpdateConfig{
			ReadModifyUpdate: true,
		},
		Response: &yamlResponse{
			IDField: "id",
		},
		Output: &yamlOutputConfig{
			Format: "table",
		},
		Arguments: yamlArguments{
			Params: []yamlArgument{
				{
					ArgName: "arg",
					Spec: []yamlArgSpec{
						{APIField: "s"},
					},
					Choices: []yamlChoice{
						{ArgValue: "av", EnumValue: "ev", HelpText: "ch"},
					},
					ResourceSpec: &yamlResourceSpec{
						Name:       "n",
						PluralName: "pn",
						Collection: "c",
						Attributes: []yamlAttribute{
							{AttributeName: "attribute_name"},
						},
						DisableAutoCompleters: true,
					},
				},
			},
		},
	}
	if diff := cmp.Diff(want, yCmd); diff != "" {
		t.Errorf("mismatch (-want +got):\n%s", diff)
	}
}
